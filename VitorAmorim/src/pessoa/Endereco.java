package pessoa;

public class Endereco {
	public String rua;
	public String bairro;
	public String cidade;
	public int num;
	
	public Endereco(String rua, String bairro, String cidade, int num) {
		
		this.rua = rua;
		this.bairro = bairro;
		this.cidade = cidade;
		this.num = num;
	}
	
	

}
